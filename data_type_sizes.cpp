#include <iostream>

int main() {
    std::cout << "Size of char: " << sizeof(char) << " byte" << std::endl;
    std::cout << "Size of int: " << sizeof(int) << " byte" << std::endl;
    std::cout << "Size of float: " << sizeof(float) << " byte" << std::endl;
    std::cout << "Size of double: " << sizeof(double) << " byte" << std::endl;

    return 0;
}